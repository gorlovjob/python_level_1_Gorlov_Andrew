# Задание-1:
# Напишите функцию, округляющую полученное произвольное десятичное число
# до кол-ва знаков (кол-во знаков передается вторым аргументом).
# Округление должно происходить по математическим правилам (0.6 --> 1, 0.4 --> 0).
# Для решения задачи не используйте встроенные функции и функции из модуля math.

print('Задача №1')

def my_round(number, ndigits):
    '''Функция производит округление десятичного
    числа до указанного количества знаков'''

    # Представим число как строку и вычленим знаки после указанного количества знаков
    number = str(number)
    end = number[number.index('.')+1:]

    # Проверяем первую цифру вычлененных цифр.
    # Если она больше или равна 5, то округление будет в большую сторону.
    # Переменная corr генерируется в зависимости от количества знаков после запятой
    # и служит для увеличения последнего знака после запятой на 1 или на 0,
    # если округление будет в меньшую сторону.
    if int(end[ndigits:ndigits+1]) >= 5:
        corr = 0.1 ** ndigits
    else:
        corr = 0

    # Увеличиваем исходное число на переменную corr. Делаем ее строковой
    i = float(number) + corr
    i = str(i)

    # Вычленяем лишние цифры после запятой.
    number = i[:i.index('.')] + i[i.index('.'):i.index('.') + ndigits + 1]
    return float(number)

print(my_round(2.1234567, 5))
print(my_round(2.1999967, 5))
print(my_round(2.9999967, 5))

