def lucky_ticket(ticket_number):
    '''Функция берет число, вычленяет при помощи целочисленного деления
        и остатка от целочисленного деления первые и последние три цифры числа,
        суммирует и сравнивает их. Если суммы равны, выводится True'''
    sum1 = 0
    sum2 = 0
    part1 = str(ticket_number//1000)
    part2 = str(ticket_number%1000)
    
    for i in part1:
        sum1 = sum1 + int(i)
    for i in part2:
        sum2 = sum2 + int(i)
    return sum1 == sum2

